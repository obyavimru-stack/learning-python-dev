age = 2025 - year
print(f"Привет, {name}! Тебе {age} лет.")

# 5. Проверка числа (без if)
a = int(input("Введите число: "))
print("Вы ввели число:", a)
print("Положительное ли число?", a > 0)
print("Отрицательное ли число?", a < 0)
print("Равно ли нулю?", a == 0)


