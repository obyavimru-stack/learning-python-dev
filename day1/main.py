# 2. Калькулятор (умножение)
a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))
print(f"Результат умножения: {a * b}")

# 3. Конвертер температуры
cel = int(input("Введите температуру в Цельсиях: "))
far = cel * 9 / 5 + 32
print(f"Температура в Фаренгейтах: {far}")

# 4. Приветствие с годом рождения
name = input("Введите имя: ")
